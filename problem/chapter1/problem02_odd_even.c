/*
 * [문제 2] 홀수/짝수 판별
 *
 * 정수를 입력받아 홀수인지 짝수인지 출력하시오.
 * 0은 짝수로 처리한다.
 *
 * 입력: 정수 n
 * 출력: "n은 짝수입니다." 또는 "n은 홀수입니다."
 *
 * 예시 입력: 7
 * 예시 출력: 7은 홀수입니다.
 */

#include <stdio.h>

int main(void) {
    int n;

    printf("정수를 입력하세요: ");
    scanf("%d", &n);

    if (n % 2 == 0) {
        printf("%d은(는) 짝수입니다.\n", n);
    } else {
        printf("%d은(는) 홀수입니다.\n", n);
    }

    return 0;
}
